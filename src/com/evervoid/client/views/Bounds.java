package com.evervoid.client.views;

public class Bounds
{
	public final int height;
	public final int width;
	public final int x;
	public final int y;

	public Bounds(final int x, final int y, final int width, final int height)
	{
		this.x = x;
		this.y = y;
		this.height = height;
		this.width = width;
	}
}
